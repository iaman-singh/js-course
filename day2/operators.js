var x = 100;
var y = 10;

// Arithmatic operators--> ("+", "-", "*", "/")
console.log("the value of x+y is: ", x + y);
console.log("the value of x-y is: ", x - y);
console.log("the value of x*y is: ", x * y);
console.log("the value of x/y is: ", x / y);

//assignment operators--> ("=")
var c = x;
c += x; // c = c + x
c *= 2; // c = c * 2
console.log(c);

//comparison operators -->("<", ">", "<=", ">=", "==", "!=")
console.log(x < y); //false
console.log(x > y); //true
console.log(x <= y); //false
console.log(x >= y); //true
console.log(x == y); //false
console.log(x != y); //true

//Logical operators--> ("&&", "||")
// 1. logical AND (&&) -->gives output true only when both conditions are true.
// 2. logical OR (||) -->gives output true when one of the two conditions are true.